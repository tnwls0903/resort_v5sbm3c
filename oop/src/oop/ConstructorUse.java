package oop;

public class ConstructorUse {

  public static void main(String[] args) {
    // Constructor: 사용자 정의 데이터 타입
    Constructor constructor1 = new Constructor(); // 객체 생성
    Constructor constructor2 = new Constructor("LG Gram");
    Constructor constructor3 = new Constructor("LG Gram", "i9");
    Constructor constructor4 = new Constructor("LG Gram", "i9", 32);
    Constructor constructor5 = new Constructor("LG Gram", "i9", 32, 2000);
    Constructor constructor6 = new Constructor("LG Gram", "i9", 32, 2000, true);
    Constructor constructor7 = new Constructor("LG Gram", "i9", 32, 2000, true, "RTX4090 super");
    Constructor constructor8 = new Constructor("LG Gram", "i9", 32, 2000, true, "RTX4090 super", 1);
    Constructor constructor9 = new Constructor("LG Gram", "i9", 32, 2000, true, "RTX4090 super", 1, 2);
    Constructor constructor10 = new Constructor("LG Gram", "i9", 32, 2000, true, "RTX4090 super", 1, 2, true);
    Constructor constructor11 = new Constructor("LG Gram", "i9", 32, 2000, true, "RTX4090 super", 1, 2, true, "middle");
    
    
  }

}
