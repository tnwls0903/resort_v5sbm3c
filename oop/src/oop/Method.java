package oop;

public class Method {
  public int add(int a, int b) {
    int tot = a + b;
    
    return tot;
  }
  
  // sub
  public int sub(int a, int b) {
    int sub = a - b;
    
    return sub;
  }
  
  // mul
  public int mul(int a, int b) {
    int mul = a * b;
    
    return mul;
  }
  
  // div
  public int div(int a, int b) {
    int div = a / b;
    
    return div;
  }
  
  // nam
  public int nam(int a, int b) {
    int nam = a % b;
    
    return nam;
  }
  
}





